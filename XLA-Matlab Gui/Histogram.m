% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global A
[filename,path] = uigetfile({'*.jpg';'*.bmp';'*.png';'*.tif'},...
    'Chon anh');
if ~isequal(filename,0)
    A = imread([path,filename]);
    axes(handles.axes2)
    imshow(A);
else
    return
end
info = imfinfo(fullfile(path,filename));
set(handles.edit1,'String',filename);%Ten anh
set(handles.edit2,'String',info.FileSize);%Dung luong
set(handles.edit3,'String',info.Width);%Chieu rong
set(handles.edit4,'String',info.Height);%Chieu cao 
set(handles.edit5,'String',info.BitDepth);%Bit
set(handles.edit6,'String',info.ColorType);%Kieu anh


% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global A
ColorType = get(handles.edit6,'String');
figure;
switch ColorType
    case 'grayscale'
        imhist(A);
        title('Histogram Anh xam');
    case 'truecolor'
        subplot(3,1,1)
        imhist(A(:,:,1));
        title('Histogram kenh do');
        subplot(3,1,2)
        imhist(A(:,:,2));
        title('Histogram kenh xanh la cay');
        subplot(3,1,3)
        imhist(A(:,:,3));
        title('Histogram xanh da troi');
end

